-- 지방 이름 (관동, 관서 등)
CREATE TABLE province (
    province_id INT AUTO_INCREMENT PRIMARY KEY,
    name_kanji VARCHAR(50) NOT NULL,
    name_en VARCHAR(50) NOT NULL
);

-- 도도부현 명
CREATE TABLE region (
    region_id INT AUTO_INCREMENT PRIMARY KEY,
    name_kanji VARCHAR(50) NOT NULL,
    name_en VARCHAR(50) NOT NULL,
    province_id INT NOT NULL,
    FOREIGN KEY (province_id) REFERENCES province(province_id) ON DELETE CASCADE
);

-- 도도부현 밑의 도시 그룹
CREATE TABLE city (
    city_id INT AUTO_INCREMENT PRIMARY KEY,
    name_kanji VARCHAR(100) NOT NULL,
    region_id INT NOT NULL,
    FOREIGN KEY (region_id) REFERENCES region(region_id) ON DELETE CASCADE
);

-- 도시 그룹 밑의 시,구 명 
CREATE TABLE town (
    town_id INT AUTO_INCREMENT PRIMARY KEY,
    name_kanji VARCHAR(100) NOT NULL,
    city_id INT NOT NULL,
    FOREIGN KEY (city_id) REFERENCES city(city_id) ON DELETE CASCADE
);

create table users (
    user_id int auto_increment not null primary key,
    login_id varchar(50) not null unique,
    nickname varchar(100) not null unique,
    profile_image char(255) default null,
    real_name varchar(100) not null,
    email varchar(255) not null unique,
    password varchar(255) not null,
    is_host tinyint default 0 check (is_host in (0,1,2)),  -- 0:게스트, 1:호스트, 2:관리자 
    role varchar(30) default 'user' check (role in ('user','admin')),
    is_email_verified boolean default false,
    reported_count int default 0,
    is_malicious boolean default false,
    languages varchar(255) not null,
    age int check (age > 0) not null,
    nationality varchar(50) not null,
    religion varchar(100) default null,
    gender tinyint default 0 check (gender in (0,1,2)),
    bio text,
    created_at timestamp default current_timestamp,
    is_subscribed tinyint default 0 check (is_subscribed in (0,1)) -- 0:구독 안함, 1: 구독함
);

CREATE TABLE share (
    share_id INT AUTO_INCREMENT NOT NULL PRIMARY KEY,
    host_id INT NOT NULL,
    region_id INT NOT NULL,
    city_id INT NOT NULL,
    town_id INT NOT NULL,
    postal_code varchar(10) NOT NULL,  
    title VARCHAR(255) NOT NULL,
    description TEXT,
    max_guests INT NOT NULL,
    current_guests INT DEFAULT 0,
    price DECIMAL(10,2) NOT NULL,
    address VARCHAR(255) NOT NULL,
    detail_address VARCHAR(255) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    status VARCHAR(50) DEFAULT 'open' CHECK (status IN ('open','closed')),
    FOREIGN KEY (host_id) REFERENCES users(user_id),
    FOREIGN KEY (region_id) REFERENCES region(region_id),
    FOREIGN KEY (city_id) REFERENCES city(city_id),
    FOREIGN KEY (town_id) REFERENCES town(town_id)
);

create table user_report (
    report_id int auto_increment not null primary key,
    reported_user_id int not null,
    reporter_user_id int not null,
    reason text not null,
	status varchar(50) default 'pending' check (status in ('pending', 'resolved', 'rejected')),
    created_at timestamp default current_timestamp,
    foreign key (reported_user_id) references users(user_id) on delete cascade,
    foreign key (reporter_user_id) references users(user_id) on delete cascade
);

create table user_review (
    review_id int auto_increment not null primary key,
    reviewed_user_id int not null,
    reviewer_id int not null,
    share_id int not null,
    cleanliness_rating int check (cleanliness_rating between 1 and 5) default 3,
    trust_rating int check (trust_rating between 1 and 5) default 3,
    communication_rating int check (communication_rating between 1 and 5) default 3,
    manner_rating int check (manner_rating between 1 and 5) default 3,
    comment text,
    created_at timestamp default current_timestamp,
    foreign key (reviewed_user_id) references users(user_id) on delete cascade,
    foreign key (reviewer_id) references users(user_id) on delete cascade,
    foreign key (share_id) references share(share_id) on delete cascade
);

create table chat_room (
    chatroom_id int auto_increment not null primary key,
    share_id int not null,
    created_at timestamp default current_timestamp,
    foreign key (share_id) references share(share_id) on delete cascade
);

create table chat_participant (
    participant_id int auto_increment not null primary key,
    chatroom_id int not null,
    host_id int not null,  
    guest_id int not null,  
    joined_at timestamp default current_timestamp,
    foreign key (chatroom_id) references chat_room(chatroom_id) on delete cascade,
    foreign key (host_id) references users(user_id) on delete cascade,
    foreign key (guest_id) references users(user_id) on delete cascade
);

create table chat_message (
    message_id int auto_increment not null primary key,
    chatroom_id int not null,
    sender_id int not null,
    message text not null,
    file_url varchar(255) default null,
    sent_at timestamp default current_timestamp,
    foreign key (chatroom_id) references chat_room(chatroom_id) on delete cascade,
    foreign key (sender_id) references users(user_id) on delete cascade
);

create table host_invitation (
    invitation_id int auto_increment not null primary key,
    guest_id int not null,
    share_id int not null,
    status varchar(50) default 'pending' check (status in ('pending', 'accepted', 'rejected', 'expired')),
    created_at timestamp default current_timestamp,
    message text,
    foreign key (guest_id) references users(user_id) on delete cascade,
    foreign key (share_id) references share(share_id) on delete cascade
);

create table viewing (
    viewing_id int auto_increment not null primary key,
    share_id int not null,
    guest_id int not null,
    is_completed boolean default false,
    scheduled_date timestamp not null,
    created_at timestamp default current_timestamp,
    foreign key (share_id) references share(share_id) on delete cascade,
    foreign key (guest_id) references users(user_id) on delete cascade
);

create table reputation_detail (
    reputation_id int auto_increment not null primary key,
    user_review_id int not null,
    type varchar(50) check (type in ('positive', 'negative', 'mismatch')),
    description text not null,
    foreign key (user_review_id) references user_review(review_id) on delete cascade
);

create table review_reply (
    reply_id int auto_increment not null primary key,
    review_id int not null,
    reply_user_id int not null,
    content text not null,
    created_at timestamp default current_timestamp,
    foreign key (review_id) references user_review(review_id) on delete cascade,
    foreign key (reply_user_id) references users(user_id) on delete cascade
);

create table user_preference (
    preference_id int auto_increment not null primary key,
    user_id int not null,
    tag varchar(255) not null,
    foreign key (user_id) references users(user_id) on delete cascade
);

create table favorite (
    favorite_id int auto_increment not null primary key,
    user_id int not null,
    share_id int not null,
    created_at timestamp default current_timestamp,
    foreign key (user_id) references users(user_id) on delete cascade,
    foreign key (share_id) references share(share_id) on delete cascade,
    unique (user_id, share_id)
);

-- 이메일 인증 테이블
create table email_auth(
   email_auth_id int auto_increment PRIMARY KEY,
    email varchar(255) not null,
    auth_code varchar(255) not null,
    expired_at timestamp not null,
    verified boolean default false,
    foreign key (email) references users(email) on delete cascade
);

